# ══════════════════════════════════════════════════════════════════════════

def _roi_trimesh(xy, faces, roi_verts_hemi, n_rings=3):
    """
    Keep only faces within n_rings of connectivity from any ROI vertex.
    This correctly handles non-convex ROI layouts (like LH flat map)
    without pulling in vast empty cortex between distant ROI clusters.

    Steps:
    1. Seed: all ROI vertices
    2. Expand by n_rings of face-adjacency (each ring adds faces that
       share a vertex with the current frontier)
    3. Return Triangulation of those faces + bool mask of included verts
    """
    # seed set — all ROI vertices, excluding tiny orphan clusters
    MIN_SEED = 50
    seed = set()
    for rt in ROI_TYPES:
        for rname, vset in roi_verts_hemi[rt].items():
            if len(vset) >= MIN_SEED:
                seed.update(vset)
    seed = np.array(sorted(seed), dtype=np.int32)

    # build vertex → face lookup
    n_faces = len(faces)
    from collections import defaultdict
    v2f = defaultdict(list)
    for fi, (a,b,c) in enumerate(faces):
        v2f[a].append(fi); v2f[b].append(fi); v2f[c].append(fi)

    # expand rings
    included_faces = set()
    frontier_verts = set(seed.tolist())
    for _ in range(n_rings):
        new_verts = set()
        for v in frontier_verts:
            for fi in v2f[v]:
                if fi not in included_faces:
                    included_faces.add(fi)
                    new_verts.update(faces[fi].tolist())
        frontier_verts = new_verts - frontier_verts
        if not frontier_verts:
            break

    crop_faces = faces[np.array(sorted(included_faces), dtype=np.int32)]
    # which vertices are used
    used_verts = np.zeros(len(xy), dtype=bool)
    used_verts[crop_faces.ravel()] = True
    tri = mtri.Triangulation(xy[:,0], xy[:,1], crop_faces)
    return tri, used_verts

