GRID_RES = 1000

def _roi_pixel_mask(xy, vert_indices, xmin, xmax, ymin, ymax,
                    res=GRID_RES, dilate=4):
    """Scatter ROI verts onto grid, dilate, return bool (res,res)."""
    idx = vert_indices
    px  = np.clip(((xy[idx,0]-xmin)/(xmax-xmin)*(res-1)).astype(int),0,res-1)
    py  = np.clip(((xy[idx,1]-ymin)/(ymax-ymin)*(res-1)).astype(int),0,res-1)
    g   = np.zeros((res,res), dtype=bool)
    g[py, px] = True
    return binary_dilation(binary_fill_holes(
           binary_dilation(g, iterations=2)), iterations=dilate)

