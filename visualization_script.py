def _tight_extent(xy, roi_verts_hemi, pad_frac=0.04, min_verts=50):
    """
    Bounding box of ROI vertices, ignoring any per-region cluster
    with fewer than min_verts vertices (orphan specks like the tiny
    streams fragment that sits far from the main ROI body).
    """
    all_idx = sorted(set(
        v for rt in ROI_TYPES
        for rname, vset in roi_verts_hemi[rt].items()
        if len(vset) >= min_verts          # ← skip tiny orphan clusters
        for v in vset
    ))
    if not all_idx:                        # fallback: use everything
        all_idx = sorted(set(
            v for rt in ROI_TYPES
            for vset in roi_verts_hemi[rt].values()
            for v in vset))
    idx = np.array(all_idx, dtype=np.int32)
    xs, ys = xy[idx, 0], xy[idx, 1]
    xw, yw = xs.max()-xs.min(), ys.max()-ys.min()
    return (float(xs.min()-xw*pad_frac), float(xs.max()+xw*pad_frac),
            float(ys.min()-yw*pad_frac), float(ys.max()+yw*pad_frac))

