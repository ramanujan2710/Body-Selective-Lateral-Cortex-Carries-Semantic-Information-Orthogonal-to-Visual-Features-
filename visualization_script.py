def _sig_marker(q_ttest, q_signflip, sig_both):
    """
    Corrected significance marker.
    *** = BH-FDR t-test q < 0.001
    **  = BH-FDR t-test q < 0.01
    *   = sign-flip q < 0.05 AND t-test q < 0.05 (sig_both == True)
    †   = t-test q < 0.05 only
        = ns
    Sign-flip uses 2^8 = 256 permutations → min achievable q_signflip ≈ 0.027,
    so ** and *** must use q_ttest to be reachable.
    """
    if   q_ttest  < 0.001:                    return '***'
    elif q_ttest  < 0.01:                     return '**'
    elif sig_both and q_signflip < 0.05:      return '*'
    elif q_ttest  < 0.05:                     return '†'
    else:                                      return ''

def _get_t3(hemi, rname, wkey, col):
    """Safe scalar lookup into T3."""
    rows = T3[(T3['hemi'] == hemi) &
              (T3['roi_name'] == rname) &
              (T3['wipe_key'] == wkey)]
    return float(rows[col].iloc[0]) if not rows.empty else np.nan

def _get_marker(hemi, rname, wkey):
    rows = T3[(T3['hemi'] == hemi) &
              (T3['roi_name'] == rname) &
              (T3['wipe_key'] == wkey)]
    if rows.empty: return ''
    r = rows.iloc[0]
    return _sig_marker(float(r['q_ttest']), float(r['q_signflip']), bool(r['sig']))

def _draw_group_dividers(ax, orientation='horizontal', color='#aaaaaa',
                         lw=1.0, ls=':'):
    """Draw divider lines between ROI type groups."""
    prev_rt = None
    for xi, (_, rt) in enumerate(ROI_ORDER):
        if rt != prev_rt and xi > 0:
            pos = xi - 0.5
            if orientation == 'horizontal':
                ax.axhline(pos, color=color, lw=lw, ls=ls, zorder=0)
            else:
                ax.axvline(pos, color=color, lw=lw, ls=ls, zorder=0)
        prev_rt = rt

