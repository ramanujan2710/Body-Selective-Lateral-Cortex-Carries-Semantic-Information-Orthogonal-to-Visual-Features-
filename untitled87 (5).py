# MASTER 2 — Group ROI table (primary reporting table)
# ═════════════════════════════════════════════════════════════════════════
print("\nBuilding MASTER_group_roi.csv...")

M2 = T_group.copy()

# Add readable significance stars column
def sig_stars(row):
    p = row['p_signflip'] if has_signflip else row['p_val']
    q = row['q_signflip'] if has_signflip else row['q_fdr']
    if q < 0.001: return '***'
    if q < 0.01:  return '**'
    if q < 0.05:  return '*'
    return 'ns'

M2['sig_label'] = M2.apply(sig_stars, axis=1)

# Rename columns for clarity
rename_map = {
    'sig':           'sig_ttest_fdr',
    'r2_wiped_mean': 'r2_wiped_mean',
    'r2_wiped_sem':  'r2_wiped_sem_across_subjects',
    'r2_raw_mean':   'r2_raw_mean',
    'r2_raw_sem':    'r2_raw_sem_across_subjects',
}
M2 = M2.rename(columns=rename_map)

# Column order for reporting
col_order_m2 = [
    'hemi','roi_type','roi_name','n_subjects',
    'r2_wiped_mean','r2_wiped_sem_across_subjects',
    'r2_raw_mean','r2_raw_sem_across_subjects',
    'ratio_mean','ratio_sem',
    't_stat','p_val','q_fdr','sig_ttest_fdr',
]
if has_signflip:
    col_order_m2 += ['p_signflip','q_signflip','sig_signflip']
col_order_m2 += ['hedges_g','sig_label']

M2 = M2[[c for c in col_order_m2 if c in M2.columns]]
M2 = M2.sort_values('r2_wiped_mean', ascending=False).reset_index(drop=True)

M2.to_csv(os.path.join(TABLES_DIR, 'MASTER_group_roi.csv'), index=False)
print(f"  Saved MASTER_group_roi.csv  ({len(M2)} rows, {len(M2.columns)} columns)")

