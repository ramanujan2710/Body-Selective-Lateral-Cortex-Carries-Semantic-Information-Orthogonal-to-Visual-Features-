def _hedges_g(vals):
    n = len(vals); m = float(np.mean(vals)); s = float(np.std(vals, ddof=1))
    if s < 1e-12 or n < 2: return 0.0
    return (m / s) * (1.0 - 3.0 / (4.0 * (n - 1) - 1))

