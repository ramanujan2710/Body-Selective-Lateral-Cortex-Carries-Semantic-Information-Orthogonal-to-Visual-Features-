"""
NSD SIGN-FLIP TEST — standalone block
======================================
Run this AFTER nsd_pipeline_v9.py has finished.

Requires in memory (already present if run in same Colab session):
  T1          — per-subject ROI table (built in Chunk 6)
  T_group     — group ROI table with t-test results (built in Chunk 7)
  TABLES_DIR  — path to nsd_results_v9/tables/
  N_SUBJ      — number of subjects

If running in a fresh session, set LOAD_FROM_CSV = True below and
point TABLES_DIR to your Drive path — the block will reload T1 and
T_group from the saved CSVs.

What this does
--------------
For each ROI × hemisphere:
  1. Collect the N subject-level r2_wiped_mean values.
  2. Enumerate all 2^N sign-flip configurations (exact, no sampling).
  3. For each configuration, flip the sign of a random subset of values
     and compute the group mean — building the exact null distribution
     of the mean under H0: signs are exchangeable (i.e. effects are
     equally likely to be positive or negative).
  4. p_signflip = fraction of null means >= observed mean (one-tailed,
     since our directional prediction is r2_wiped > 0).
  5. BH-FDR across all ROI × hemi p_signflip values.

The result is appended to T_group as two new columns:
  p_signflip   — exact sign-flip p-value
  q_signflip   — BH-FDR corrected

T3_group_roi_r2.csv is overwritten with the enriched table.
A new T3b_signflip_summary.csv is also written with just the key columns.

With N=7: 2^7 = 128 permutations. Minimum achievable p = 1/128 ≈ 0.0078.
Runtime: negligible (pure numpy, no regression).
"""

# ── Config ────────────────────────────────────────────────────────────────
