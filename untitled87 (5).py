# Join alpha_wipe and r2_vis_bert from T2
T2_sub = T2[['subject','hemi','alpha_wipe','r2_vis_bert',
             'n_voxels']].copy()
T2_sub = T2_sub.rename(columns={'n_voxels': 'n_voxels_total'})

M1 = T1.merge(T2_sub, on=['subject','hemi'], how='left')

# Add group significance flags per ROI so subject rows know if their ROI
# is group-significant
grp_sig = T_group[['hemi','roi_name','sig']].copy()
grp_sig = grp_sig.rename(columns={'sig': 'group_sig_ttest'})
if has_signflip:
    grp_sig2 = T_group[['hemi','roi_name','sig_signflip']].copy()
    grp_sig  = grp_sig.merge(grp_sig2, on=['hemi','roi_name'], how='left')

M1 = M1.merge(grp_sig, on=['hemi','roi_name'], how='left')

# Clean column order
col_order_m1 = [
    'subject','hemi','roi_type','roi_name',
    'n_voxels','n_voxels_total',
    'r2_wiped_mean','r2_wiped_median','r2_wiped_std','r2_wiped_p95',
    'r2_raw_mean','delta_r2_mean','frac_pos_wiped',
    'ratio',
    'alpha_wipe','r2_vis_bert',
    'group_sig_ttest',
]
if has_signflip:
    col_order_m1.append('sig_signflip')
M1 = M1[[c for c in col_order_m1 if c in M1.columns]]
M1 = M1.sort_values(['roi_type','roi_name','hemi','subject']).reset_index(drop=True)

M1.to_csv(os.path.join(TABLES_DIR, 'MASTER_subject_roi.csv'), index=False)
print(f"  Saved MASTER_subject_roi.csv  ({len(M1)} rows, {len(M1.columns)} columns)")

