def _encode_bert(texts):
    parts = []
    for bi in range(0, len(texts), BERT_BATCH):
        enc = _bert_tok(texts[bi:bi+BERT_BATCH], padding=True,
                        truncation=True, max_length=512, return_tensors='pt')
        with torch.no_grad():
            out = _bert_mdl(**{k: v.to(DEVICE) for k, v in enc.items()})
        parts.append(out.last_hidden_state[:, 0, :].cpu().numpy())
    return np.vstack(parts).astype(np.float32)

def _encode_gpt2(texts):
    parts = []
    for bi in range(0, len(texts), BERT_BATCH):
        enc = _gpt2_tok(texts[bi:bi+BERT_BATCH], padding=True,
                        truncation=True, max_length=512, return_tensors='pt')
        enc = {k: v.to(DEVICE) for k, v in enc.items()}
        with torch.no_grad():
            out = _gpt2_mdl(**enc)
        # Last non-pad token: use attention_mask to find it
        hs   = out.last_hidden_state          # (B, T, 768)
        mask = enc['attention_mask']          # (B, T)
        last_idx = mask.sum(dim=1) - 1        # index of last real token
        batch_idx = torch.arange(hs.size(0), device=hs.device)
        parts.append(hs[batch_idx, last_idx, :].cpu().numpy())
    return np.vstack(parts).astype(np.float32)

def _encode_clip_text(texts):
    parts = []
    for bi in range(0, len(texts), BERT_BATCH):
        enc = _clip_proc(text=texts[bi:bi+BERT_BATCH], return_tensors='pt',
                         padding=True, truncation=True, max_length=77)
        input_ids      = enc['input_ids'].to(DEVICE)
        attention_mask = enc['attention_mask'].to(DEVICE)
        with torch.no_grad():
            txt_out = _clip_model.text_model(
                input_ids=input_ids, attention_mask=attention_mask)
            # pooler_output = last [EOS] token hidden state → text_projection → 512-dim
            feat = _clip_model.text_projection(txt_out.pooler_output)
        parts.append(feat.cpu().numpy())
    return np.vstack(parts).astype(np.float32)

