def _extract_l2_ensemble(img_bytes_list, N_s):
    """
    Extract and concatenate all 4 L2 visual models.
    Returns (N, 512+2048+512+1280) = (N, 4352) float32.
    """
    vgg_out, res_out, clip_out, eff_out = [], [], [], []
    for bi in range(0, N_s, IMG_BATCH):
        batch_pil = [Image.open(io.BytesIO(img_bytes_list[i])).convert('RGB')
                     for i in range(bi, min(bi+IMG_BATCH, N_s))]

        # VGG19 + ResNet50 share the same 224×224 normalised transform
        t224 = torch.stack([_img_tf_224(p) for p in batch_pil]).to(DEVICE)
        with torch.no_grad():
            vgg_out.append(_vgg_net(t224).cpu().numpy())
            _resnet(t224)
            res_out.append(_resnet_feats['out'].cpu().numpy())

        # CLIP visual — use vision_model directly, take pooler_output (512-dim)
        clip_in = _clip_proc(images=batch_pil, return_tensors='pt')
        pv = clip_in['pixel_values'].to(DEVICE)
        with torch.no_grad():
            vis_out = _clip_model.vision_model(pixel_values=pv)
            # pooler_output is the [CLS] token projected — shape (B, 768)
            # get_image_features applies the visual projection → 512-dim
            cv = _clip_model.visual_projection(vis_out.pooler_output)
            clip_out.append(cv.cpu().numpy())

        # EfficientNet-B0 — timm's default transform (224×224 same norm)
        with torch.no_grad():
            eff_out.append(_effnet(t224).cpu().numpy())

    vgg_arr  = np.vstack(vgg_out).astype(np.float32)
    res_arr  = np.vstack(res_out).astype(np.float32)
    clip_arr = np.vstack(clip_out).astype(np.float32)
    eff_arr  = np.vstack(eff_out).astype(np.float32)
    combined = np.hstack([vgg_arr, res_arr, clip_arr, eff_arr])
    print(f"    L2a dims — VGG:{vgg_arr.shape[1]} ResNet:{res_arr.shape[1]} "
          f"CLIP-vis:{clip_arr.shape[1]} EffNet:{eff_arr.shape[1]} "
          f"total:{combined.shape[1]}")
    return combined

# ── L2b: VGG19 multi-layer global-average-pool ───────────────────────────
# Hooks on three intermediate relu outputs:
#   features[18] = relu3_4  → 256 channels
#   features[27] = relu4_4  → 512 channels
#   features[36] = relu5_4  → 512 channels
# Each spatial map is global-average-pooled → scalar per channel.
# Concat → 256+512+512 = 1280-dim.
# This captures hierarchical mid-level visual features absent from avgpool alone.
_vgg_full = models.vgg19(weights=models.VGG19_Weights.IMAGENET1K_V1).to(DEVICE).eval()
_vgg_layer_acts = {}

def _make_vgg_hook(name):
    def hook(m, i, o):
        # o shape: (B, C, H, W) → GAP → (B, C)
        _vgg_layer_acts[name] = o.mean(dim=(-2, -1))
    return hook

_vgg_full.features[18].register_forward_hook(_make_vgg_hook('relu3'))
_vgg_full.features[27].register_forward_hook(_make_vgg_hook('relu4'))
_vgg_full.features[36].register_forward_hook(_make_vgg_hook('relu5'))

def _extract_l2b_vgg_multilayer(img_bytes_list, N_s):
    """
    VGG19 multi-layer GAP features.
    Returns (N, 1280) float32  [256 + 512 + 512].
    """
    r3_out, r4_out, r5_out = [], [], []
    for bi in range(0, N_s, IMG_BATCH):
        batch_pil = [Image.open(io.BytesIO(img_bytes_list[i])).convert('RGB')
                     for i in range(bi, min(bi+IMG_BATCH, N_s))]
        t224 = torch.stack([_img_tf_224(p) for p in batch_pil]).to(DEVICE)
        with torch.no_grad():
            _vgg_full(t224)   # forward triggers all hooks
        r3_out.append(_vgg_layer_acts['relu3'].cpu().numpy())
        r4_out.append(_vgg_layer_acts['relu4'].cpu().numpy())
        r5_out.append(_vgg_layer_acts['relu5'].cpu().numpy())
    r3 = np.vstack(r3_out).astype(np.float32)
    r4 = np.vstack(r4_out).astype(np.float32)
    r5 = np.vstack(r5_out).astype(np.float32)
    combined = np.hstack([r3, r4, r5])
    print(f"    L2b dims — relu3:{r3.shape[1]} relu4:{r4.shape[1]} "
          f"relu5:{r5.shape[1]} total:{combined.shape[1]}")
    return combined

