def _r2_batch(Y_block, X, alpha=RIDGE_ALPHA, n_splits=CMI_FOLDS):
    N_  = Y_block.shape[0]
    kf  = KFold(n_splits=n_splits, shuffle=True, random_state=RANDOM_STATE)
    ss_res = np.zeros(Y_block.shape[1], dtype=np.float64)
    ss_tot = np.zeros(Y_block.shape[1], dtype=np.float64)
    for tr, te in kf.split(np.arange(N_)):
        sc   = StandardScaler().fit(X[tr])
        rid  = Ridge(alpha=alpha, fit_intercept=True).fit(sc.transform(X[tr]), Y_block[tr])
        pred = rid.predict(sc.transform(X[te]))
        res  = Y_block[te] - pred
        tot  = Y_block[te] - Y_block[te].mean(0, keepdims=True)
        ss_res += (res**2).sum(0)
        ss_tot += (tot**2).sum(0)
    return (1.0 - ss_res / (ss_tot + 1e-12)).astype(np.float32)

def _run_voxelwise_r2(fmri, X, desc=''):
    V      = fmri.shape[1]
    blocks = [(i, min(i+VOXEL_BLOCK, V)) for i in range(0, V, VOXEL_BLOCK)]
    print(f"  {desc}: {V} vox | {len(blocks)} blocks {elapsed()}")
    out = np.concatenate(
        Parallel(n_jobs=N_JOBS, prefer='threads')(
            delayed(_r2_batch)(fmri[:, s:e], X) for s, e in blocks))
    print(f"  {desc} done | mean_R²={out.mean():.5f} max_R²={out.max():.5f} {elapsed()}")
    return out

