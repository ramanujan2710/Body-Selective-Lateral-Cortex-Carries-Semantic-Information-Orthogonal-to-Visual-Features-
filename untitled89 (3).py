# ── L1: Gabor filterbank ──────────────────────────────────────────────────
def _make_gabors(ksize=21, n_or=8, n_sc=4):
    gs = []
    for sc in range(n_sc):
        sig, lam = 2**sc, 2**(sc+1)
        for o in range(n_or):
            k = cv2.getGaborKernel((ksize, ksize), sig, o*np.pi/n_or, lam, 0.5, 0)
            k /= np.abs(k).sum() + 1e-10
            gs.append(k)
    return gs

_GF = _make_gabors()

def _extract_l1(ib):
    """
    Full Gabor + colour histogram + edge features.
    Output dim = n_filters × n_scales × 2 stats  +  colour patches  +  edge cells
    Fixed for all images; exact dim printed at runtime.
    """
    img  = Image.open(io.BytesIO(ib)).convert('RGB')
    a    = np.array(img)
    gray = cv2.cvtColor(a, cv2.COLOR_RGB2GRAY).astype(np.float32)
    h, w = gray.shape
    feats = []

    # Gabor responses — mean + std in spatial grid (1×1, 2×2, 4×4)
    for gf in _GF:
        r = np.abs(cv2.filter2D(gray, cv2.CV_32F, gf))
        for lv in range(3):
            g = 2**lv
            for ri in range(g):
                for ci_ in range(g):
                    cell = r[ri*h//g:(ri+1)*h//g, ci_*w//g:(ci_+1)*w//g]
                    feats += [np.mean(cell), np.std(cell)]

    # HSV colour histograms in 2×2 spatial grid (16 bins × 3 channels × 4 patches)
    hsv = cv2.cvtColor(a, cv2.COLOR_RGB2HSV).astype(np.float32)
    for qi in range(2):
        for qj in range(2):
            patch = hsv[qi*h//2:(qi+1)*h//2, qj*w//2:(qj+1)*w//2]
            for ch in range(3):
                feats.extend(cv2.calcHist([patch], [ch], None, [16], [0, 256]).flatten())

    # Canny edge density in 2×2 grid, 3 threshold pairs
    for lo, hi in [(50, 100), (100, 200), (150, 300)]:
        edges = cv2.Canny(gray.astype(np.uint8), lo, hi)
        for qi in range(2):
            for qj in range(2):
                feats.append(
                    np.mean(edges[qi*h//2:(qi+1)*h//2, qj*w//2:(qj+1)*w//2] > 0))

    return np.array(feats, dtype=np.float32)

