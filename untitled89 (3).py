def _hedges_g(vals):
    n = len(vals); m = float(np.mean(vals)); s = float(np.std(vals, ddof=1))
    if s < 1e-12 or n < 2: return 0.0
    return (m / s) * (1.0 - 3.0 / (4.0*(n-1) - 1))

def _sign_flip_p(vals):
    """Exact one-tailed sign-flip test. H0: mean(vals) <= 0."""
    vals = np.asarray(vals, dtype=np.float64)
    obs  = vals.mean()
    signs = np.array(list(itertools.product([-1,1], repeat=len(vals))),
                     dtype=np.float64)
    null_means = (signs * vals[np.newaxis,:]).mean(axis=1)
    return float((null_means >= obs).mean())

