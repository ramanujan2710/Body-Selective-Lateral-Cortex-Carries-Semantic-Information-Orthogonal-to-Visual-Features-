from sklearn.model_selection import train_test_split

def wipe_visual_single_fold(X_vis, l3_raw, model_name,
                             train_frac=WIPE_TRAIN_FRAC,
                             alphas=RIDGECV_ALPHAS,
                             seed=RANDOM_STATE):
    """
    Single 80/20 split residualization of l3_raw against X_vis.

    Returns
    -------
    l3_wiped_te : (N_te, D) residuals on held-out test set
    te_idx      : (N_te,)  trial indices corresponding to l3_wiped_te
    alpha_best  : float
    r2_vis_tr   : float    mean R² on train set (diagnostic, in-sample)
    r2_vis_te   : float    mean R² on test set  (diagnostic, out-of-sample)
    """
    N = l3_raw.shape[0]
    tr_idx, te_idx = train_test_split(
        np.arange(N), train_size=train_frac,
        random_state=seed, shuffle=True)

    # Scale on train only
    sc = StandardScaler().fit(X_vis[tr_idx])
    X_tr = sc.transform(X_vis[tr_idx])
    X_te = sc.transform(X_vis[te_idx])

    # RidgeCV alpha selection on train set
    rcv = RidgeCV(alphas=alphas, fit_intercept=True, cv=5).fit(X_tr, l3_raw[tr_idx])
    alpha_best = float(rcv.alpha_)

    # Fit final Ridge on train, residualise test
    rid = Ridge(alpha=alpha_best, fit_intercept=True).fit(X_tr, l3_raw[tr_idx])
    pred_tr = rid.predict(X_tr)
    pred_te = rid.predict(X_te)

    # Diagnostic R²
    r2_tr = float(np.mean([
        r2_score(l3_raw[tr_idx, d], pred_tr[:, d])
        for d in range(l3_raw.shape[1])]))
    r2_te = float(np.mean([
        r2_score(l3_raw[te_idx, d], pred_te[:, d])
        for d in range(l3_raw.shape[1])]))

    l3_wiped_te = (l3_raw[te_idx] - pred_te).astype(np.float32)

    print(f"    [{model_name}] alpha={alpha_best:.1e}  "
          f"R²_vis→L3 train={r2_tr:.4f}  test={r2_te:.4f}  "
          f"N_te={len(te_idx)}")
    var_raw = float(np.var(l3_raw[te_idx]))
    var_wip = float(np.var(l3_wiped_te))
    print(f"    [{model_name}] variance retained: {var_wip/var_raw*100:.1f}%")

    return l3_wiped_te, te_idx, alpha_best, r2_tr, r2_te

