# SECTION 3 — PER-SUBJECT FULL STATE (arrays)
# ═══════════════════════════════════════════════════════════════════════════
print("\n── Section 3: Per-subject arrays ──")

# What to save per subject — every array produced at every stage
ARRAY_KEYS = {
    # ── Chunk 3: features ──────────────────────────────────────────────
    'gabor_raw':    'L1 Gabor features (N, gabor_dim)',
    'l2a':          'L2a broad ensemble features (N, l2a_dim)',
    'l2b':          'L2b VGG multi-layer GAP (N, 1280)',
    'X_vis_Xa':     'X_vis variant A = Gabor+L2a (N, Xa_dim)',
    'X_vis_Xb':     'X_vis variant B = Gabor+L2b (N, Xb_dim)',
    'bert_raw':     'BERT CLS embeddings (N, 768)',
}
# l3_raw: one per L3 model
# l3_wiped: one per wipe_key
# wipe_te_idx: one per wipe_key
# r2_wiped: hemi → wipe_key → (V,)
# r2_raw:   hemi → (V,)

for sid, sd in subj_data.items():
    print(f"  Saving {sid}...")
    state = {'subject': sid}

    # Fixed-key arrays
    for key, desc in ARRAY_KEYS.items():
        if key in sd:
            state[key] = sd[key]

    # L3 raw embeddings
    state['l3_raw'] = {m: sd['l3_raw'][m] for m in L3_MODELS}

    # Wipe outputs
    state['l3_wiped']    = {wk: sd['l3_wiped'][wk]    for wk in WIPE_KEYS}
    state['wipe_te_idx'] = {wk: sd['wipe_te_idx'][wk] for wk in WIPE_KEYS}
    state['wipe_meta']   = sd['wipe_meta']   # dict of dicts (serialisable)

    # Voxelwise R²
    state['r2_wiped'] = {
        hemi: {wk: sd['r2_wiped'][hemi][wk] for wk in WIPE_KEYS}
        for hemi in ['LH', 'RH']
    }
    state['r2_raw'] = {
        hemi: sd['r2_raw'][hemi]
        for hemi in ['LH', 'RH']
    }

    # fMRI (large — save separately, skip if memory-constrained)
    # Uncomment if you want to archive the raw fMRI too:
    # state['lh_fmri'] = sd['lh_fmri']
    # state['rh_fmri'] = sd['rh_fmri']

    # ROI masks (compact)
    state['lh_roi_challenge'] = sd.get('lh_roi_challenge', {})
    state['rh_roi_challenge'] = sd.get('rh_roi_challenge', {})

    pkl_path = os.path.join(ARCHIVE_DIR, f'{sid}_full_state.pkl')
    with open(pkl_path, 'wb') as f:
        pickle.dump(state, f, protocol=4)
    size_mb = os.path.getsize(pkl_path) / 1e6
    print(f"    {sid}_full_state.pkl  ({size_mb:.0f} MB)")

